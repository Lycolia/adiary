window.$secure = function(id) {
	var obj = $(document).find('[id="' + id.substr(1) + '"]');
	if (obj.length >1) {
		adiary.show_error('Security Error!<p>id="' + id + '" is duplicate.</p>');
		return $([]);		// 2つ以上発見された
	}
	return obj;
}

